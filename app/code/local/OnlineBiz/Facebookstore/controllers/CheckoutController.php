     public function forgotpasswordAction()
     {
		$session = Mage::getSingleton('core/session');
        $email = $this->getRequest()->getPost('email');

        if (!Zend_Validate::is($email, 'EmailAddress')) {
            $result = array('success'=>false);
			$session->addError($this->__('Please enter your email'));
			$this->_redirect('facebookstore/checkout/getpassword');
        }else{

            $customer = Mage::getModel('customer/customer')
            ->setWebsiteId(Mage::app()->getStore()->getWebsiteId())
            ->loadByEmail($email);

            if ($customer->getId()) {
                try {
                    $newPassword = $customer->generatePassword();
                    $customer->changePassword($newPassword, false);
                    $customer->sendPasswordReminderEmail();
                    $result = array('success'=>true);
					Mage::getSingleton('customer/session')->addSuccess($this->__('A new password has been sent.'));
					$this->_redirect('facebookstore/checkout');
                }
                catch (Exception $e){
                    $result = array('success'=>false, 'error'=>$e->getMessage());
					$session->addError($this->__('This email address was not found in our records'));
					$this->_redirect('facebookstore/checkout/getpassword');
                }
            }else {
				
                $result = array('success'=>false, 'error'=>'This email address was not found in our records');
				$session->addError($this->__('This email address was not found in our records'));
				$this->_redirect('facebookstore/checkout/getpassword');
            }
        }
		
    }


    /**
     * Check can page show for unregistered users
     *
     * @return boolean
     */
    protected function _canShowForUnregisteredUsers()
    {
		if(Mage::getVersion() < 1.7){
			return Mage::getSingleton('customer/session')->isLoggedIn()
				|| $this->getRequest()->getActionName() == 'index'
				|| Mage::helper('checkout')->isAllowedGuestCheckout($this->getOnepage()->getQuote());
		} else {
			return Mage::getSingleton('customer/session')->isLoggedIn()
				|| $this->getRequest()->getActionName() == 'index'
				|| Mage::helper('checkout')->isAllowedGuestCheckout($this->getOnepage()->getQuote())
				|| !Mage::helper('checkout')->isCustomerMustBeLogged();
		}
    }
